package de.doubleslash.demo.multimodule.coverage.four.it;

import de.doubleslash.demo.multimodule.coverage.four.models.Project;
import de.doubleslash.demo.multimodule.coverage.four.repositories.ProjectRepository;
import org.junit.After;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.orm.jpa.DataJpaTest;
import org.springframework.test.context.junit4.SpringRunner;

import static org.assertj.core.api.BDDAssertions.then;

@RunWith(SpringRunner.class)
@DataJpaTest
public class ProjectRepositoryIT {
    @Autowired
    private ProjectRepository systemUnderTest;

    @After
    public void tearDown() throws Exception {
        // This can be used here as `deleteAll()` is generated by Spring Data
        systemUnderTest.deleteAll();
    }

    @Test
    public void should_ReturnPersistedProject_When_QueriedById() {
        // given
        Project project = new Project();
        project.setName("test");
        project = systemUnderTest.save(project);

        // when
        Project actualProject = systemUnderTest.getOne(project.getId());

        // then
        then(actualProject)
                .isEqualTo(project);
    }
}
